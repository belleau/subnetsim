#' subnetsim: Testing if a subnetwork is significantly more connected
#' then randomly selected subnetworks with the same number of nodes.
#'
#' This package tests the hypothesis that a specific subnetwork is more connected
#' than randomly selected subnetworks with the same number of nodes. Three
#' measures of connectivity are validated: the number of links starting from the nodes
#' of the subnetwork, the number of nodes plus the number of nodes
#' directly connected to the subnetwork and the number of links from nodes
#' of the subnetwork and the nodes directly connected to the subnetwork.
#' To test if the subnetwork is significantly more connected than connections
#' obtained by randomness, the package does a simulation analysis.
#'
